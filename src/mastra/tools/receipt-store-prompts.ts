/**
 * 店舗別レシート解析プロンプト
 *
 * parse-receipt ツールが2段階方式で使用する:
 * 1. 画像から店舗名を識別
 * 2. resolveStorePrompt(storeName) で店舗専用プロンプトを取得し、詳細解析
 */

const BASE_PROMPT = `このレシート画像から食材リストを抽出してください。

## 基本ルール
- 食材でないもの（レジ袋代、ポイント、割引行、値引行、消費税、小計以降の行、ボーナスポイント行）は is_food: false にしてください。
- 数量が不明な場合は 1 としてください。
- 単位は「個」「g」「ml」「本」「パック」「袋」「枚」「切れ」等を使ってください。

## 略称の展開（重要）
スーパーのレシートでは商品名が省略されています。以下のルールで正式な食材名に補完してください:
- 末尾が切り詰められるパターンが多い（例: スパゲッティ→スパ、エリンギ→エリ、担々麺→担）
- ブランド名プレフィックスは正式メーカー名への手がかりとして使う（商品名からは除外してよい）
- 商品名末尾の数字は個数・食数として quantity に設定する（例: 「たまご白10」→ quantity: 10、「うどん5食」→ quantity: 5）
- 「2コX単198」のような複数購入行は直前の商品の quantity に反映してください。`;

const SUMMIT_PROMPT = `

## サミット固有のルール

### 部門コード（左端の4桁数字）
商品カテゴリの推論に活用してください:
- 1100 = 青果（野菜・きのこ）
- 1300 = 精肉
- 1520 = 加工食品（常温: 乾物・調味料・飲料・麺類）
- 1580 = 日配品（冷蔵: 納豆・豆腐・卵・麺・デザート等）
- 4425 = パン・ベーカリー
例: 部門1520の「たらこ」は生鮮たらこではなく加工食品（パスタソース等）と判断できます。

### ブランド名辞書
- SB = S&B食品（スパイス・調味料・パスタソース）→ 「SB生風味たらこ」= たらこパスタソース
- ホクレン = ホクレン農業協同組合連合会（片栗粉・小麦粉等）
- ユウキ = ユウキ食品（中華・エスニック系調味料）
- 日清 = 日清食品（麺類・即席食品）→ 「日清中華 汁なし担」= 汁なし担々麺
- くらし = くらし良好（サミットPBブランド）→ 「くらしカットエリ」= カットエリンギ
- 水彩の森 = サミットPB天然水（ブランド名=商品名）→ 食材名は「天然水」
- こくうま = 東海漬物のキムチブランド
- 丸鶏がら = 味の素の中華スープブランド

### 特殊行
- 「2コX単198」= 直前の商品が2個×@198円。quantity に反映
- 「単品ボーナスポイント」行 → スキップ（is_food: false）
- 「軽*」= 軽減税率マーク → 無視してよい`;

const STORE_PROMPTS: Record<string, string> = {
  サミット: SUMMIT_PROMPT,
  // 将来: マルエツ, ライフ 等を追加
};

/**
 * 店舗名から適切なプロンプトを解決する。
 * 部分一致で検索するため「サミットストア」「サミット井土ヶ谷店」等でもマッチする。
 * 未知の店舗の場合は汎用ベースプロンプトのみ返す。
 */
export function resolveStorePrompt(storeName: string): string {
  const matchedKey = Object.keys(STORE_PROMPTS).find((key) =>
    storeName.includes(key),
  );
  const storeSpecific = matchedKey ? STORE_PROMPTS[matchedKey] : '';
  return BASE_PROMPT + storeSpecific;
}
